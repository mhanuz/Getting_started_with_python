x=10
if x<20: # less than operator
    print(x,' is less than 20')

if x>5: # greater than operator
    print(x,'is greater than 5')

if x<=20: # less than or equal
    print(x,'is less than or equal 20')

if x>=5: # greater than or equal
    print(x,'is greater than or equal 5')
if x==10: # equal
    print(x,'is equal 10')
if x!=5:# not equal
    print(x,'is not equal 5')
    